package com.example.common.config;

/**
 * CSV出力時のクオート戦略
 * 
 * <p>CSVファイルのフィールドをダブルクォートで囲むかどうかの戦略を定義します。</p>
 */
public enum QuoteStrategy {
    /**
     * 全てのフィールドをクオートする（デフォルト）
     * <p>OpenCSVのデフォルトの挙動です。</p>
     */
    ALL,
    
    /**
     * 必要なフィールドのみクオートする
     * <p>区切り文字や改行文字を含むフィールドのみクオートします。</p>
     */
    MINIMAL,
    
    /**
     * クオートしない
     * <p>いかなる場合もクオートしません。データに区切り文字が含まれる場合、CSVが壊れる可能性があります。</p>
     */
    NONE
}

